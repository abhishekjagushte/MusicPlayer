    void print()
    {
        textArea.setText(null);
        for(int i=0;i<=r;i++)
        {
            if(f!=i)
                textArea.append(i+1+"]  "+ name[i]+"\n");
            else
                textArea.append(i+1+"]  "+ name[i]+"\t(Playing Right Now)\n");
        }
    }
    
    void shift(int s)
    {
        for(int i=r;i>=s;i--)
        {
           name[i+1]=name[i];
           Playlist[i+1]=Playlist[i];
        }
        r++;
    }
    
    
