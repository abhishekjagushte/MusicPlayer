     void pause() throws InterruptedException, JavaLayerException, IOException
	{
		if(Playing)
                {
                    pauseLocation=fis.available();
                    System.out.println("The Pause Location: "+pauseLocation);
                    player.close();	
                    Playing=false;
                    jButton1.setText("Play");
                }
	}
    
    void play() throws JavaLayerException, IOException, InterruptedException
	{
		
            
            if(!Playing)
                {
                    //filename="07.  Breathe (feat. Colbie Caillat) [feat. Colbie Caillat] - Taylor Swift (320kbps).mp3";
                    fis = new FileInputStream(Playlist[f]);
                    player = new Player(fis);
                    jLabel4.setText(name[f]);
                    jButton1.setText("Pause");
                    
                    songtotalLength=fis.available();
                    
                    if(count==0)
                    {
                        pauseLocation=songtotalLength;
                        //pauseLocation=569186;
                    }
                    
                    try {
			fis.skip(songtotalLength-pauseLocation-15000);
		} catch (IOException e1) {
			
			
		}
                    
          
                new Thread()
                {
                    public void run()
                    {
                        try {
                            Playing=true;
                            count++;
                            player.play();
                           
                            
                                    } catch (JavaLayerException ex) {
                            
                        }
                        if(player.isComplete())
                        try {
                            checkComplete();
                                    
                                    } catch (InterruptedException | JavaLayerException | IOException ex) {
                            Logger.getLogger(PlayerScreen.class.getName()).log(Level.SEVERE, null, ex);
                        }}
                }.start();
                
              
                }
		
	}
    
    
    
    
    void playnext() throws JavaLayerException, IOException, InterruptedException
	{
		
            if(!Playing)
                {
                    //filename="07.  Breathe (feat. Colbie Caillat) [feat. Colbie Caillat] - Taylor Swift (320kbps).mp3";
                    if(f==r)
                        f=-1;
                    fis = new FileInputStream(Playlist[++f]);
                    player = new Player(fis);
                    jLabel4.setText(name[f]);
                    jButton1.setText("Pause");
                    
                    
                    songtotalLength=fis.available();
                    
                    
                    
                        pauseLocation=songtotalLength;
                    
                    try {
			fis.skip(songtotalLength-pauseLocation);
		} catch (IOException e1) {
			
			
		}
                    
          
                new Thread()
                {
                    public void run()
                    {
                        try {
                            Playing=true;
                            count++;
                            player.play();
                           
                            
                                    } catch (JavaLayerException ex) {
                            
                        }
                        if(player.isComplete())
                        try {
                            checkComplete();
                                    
                                    } catch (InterruptedException | JavaLayerException | IOException ex) {
                            Logger.getLogger(PlayerScreen.class.getName()).log(Level.SEVERE, null, ex);
                        }}
                }.start();
                
              
                }
		
	}
    
    
    void checkComplete() throws InterruptedException, JavaLayerException, IOException
    {
        stop();
        playnext();
        print();
    }
    
    
    

	
	void stop() throws InterruptedException, JavaLayerException
	{
		if(Playing)
                player.close();	
                Playing=false;
                jLabel4.setText("Nothing Playing right now... Hit the Play Button");
                count=0;
                jButton1.setText("Play");
	}
        
        
        
	
        


